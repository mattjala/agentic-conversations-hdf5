"""Regenerate results/results.md from the two benchmark JSON files.

Run after any benchmark update:
    python benchmarks/write_results_md.py
"""
from __future__ import annotations

import json
import statistics
from pathlib import Path

ROOT   = Path(__file__).parent.parent
REAL   = ROOT / "results" / "jsonl_vs_hdf5.json"
SYNTH  = ROOT / "results" / "benchmark_results.json"
OUT    = ROOT / "results" / "results.md"


def _pct(a, b):
    """Percentage change from a to b."""
    if a == 0:
        return "—"
    return f"{(b - a) / a * 100:+.0f}%"


def _kb(n):
    return f"{n / 1e3:.0f}"


def _ms(n):
    return f"{n:.2f}"


def main():
    real  = json.loads(REAL.read_text())
    synth = json.loads(SYNTH.read_text()) if SYNTH.exists() else {}

    lines: list[str] = []
    w = lines.append

    w("# Benchmark Results")
    w("")
    w("Auto-generated by `benchmarks/write_results_md.py`. "
      "Re-run after any benchmark update.")
    w("")

    # ------------------------------------------------------------------
    # Section 1: real-session file size and latency
    # ------------------------------------------------------------------
    w("## Real-session comparison (`jsonl_vs_hdf5.py`)")
    w("")
    w("Each row is one JSONL session converted to all three HDF5 layouts. "
      "Latencies are the median of 3 cold-open reads.")
    w("")

    # --- File size table ---
    w("### File size (kB)")
    w("")
    w("| Session | JSONL raw | JSONL gz | HDF5 VLEN | HDF5 packed | HDF5 compound |")
    w("|---------|----------:|---------:|----------:|------------:|--------------:|")
    for r in real:
        name = r["session"][:20]
        w(f"| {name} "
          f"| {_kb(r['jsonl_bytes'])} "
          f"| {_kb(r['gzip_bytes'])} "
          f"| {_kb(r['hdf5_bytes'])} "
          f"| {_kb(r['packed_bytes'])} "
          f"| {_kb(r['compound_bytes'])} |")

    # summary row
    avg_h5  = statistics.mean(r["hdf5_bytes"]     for r in real)
    avg_pk  = statistics.mean(r["packed_bytes"]   for r in real)
    avg_cp  = statistics.mean(r["compound_bytes"] for r in real)
    avg_jl  = statistics.mean(r["jsonl_bytes"]    for r in real)
    avg_gz  = statistics.mean(r["gzip_bytes"]     for r in real)
    w(f"| **mean** "
      f"| **{_kb(avg_jl)}** "
      f"| **{_kb(avg_gz)}** "
      f"| **{_kb(avg_h5)}** "
      f"| **{_kb(avg_pk)}** ({_pct(avg_h5, avg_pk)} vs VLEN) "
      f"| **{_kb(avg_cp)}** ({_pct(avg_h5, avg_cp)} vs VLEN) |")
    w("")

    # --- Tail latency table ---
    w("### get_recent_context(20) latency (ms, median of 3 cold reads)")
    w("")
    w("| Session | HDF5 VLEN | HDF5 packed | HDF5 compound |")
    w("|---------|----------:|------------:|--------------:|")
    for r in real:
        name = r["session"][:20]
        w(f"| {name} "
          f"| {_ms(r['tail_hdf5_ms'])} "
          f"| {_ms(r['tail_packed_ms'])} "
          f"| {_ms(r['tail_compound_ms'])} |")
    avg_th = statistics.mean(r["tail_hdf5_ms"]     for r in real)
    avg_tp = statistics.mean(r["tail_packed_ms"]   for r in real)
    avg_tc = statistics.mean(r["tail_compound_ms"] for r in real)
    w(f"| **mean** "
      f"| **{_ms(avg_th)}** "
      f"| **{_ms(avg_tp)}** ({_pct(avg_th, avg_tp)} vs VLEN) "
      f"| **{_ms(avg_tc)}** ({_pct(avg_th, avg_tc)} vs VLEN) |")
    w("")

    # --- Usage query latency table ---
    w("### total_usage() latency (ms, median of 3 cold reads)")
    w("")
    w("| Session | HDF5 VLEN | HDF5 packed | HDF5 compound |")
    w("|---------|----------:|------------:|--------------:|")
    for r in real:
        name = r["session"][:20]
        w(f"| {name} "
          f"| {_ms(r['usage_hdf5_ms'])} "
          f"| {_ms(r['usage_packed_ms'])} "
          f"| {_ms(r['usage_compound_ms'])} |")
    avg_uh = statistics.mean(r["usage_hdf5_ms"]     for r in real)
    avg_up = statistics.mean(r["usage_packed_ms"]   for r in real)
    avg_uc = statistics.mean(r["usage_compound_ms"] for r in real)
    w(f"| **mean** "
      f"| **{_ms(avg_uh)}** "
      f"| **{_ms(avg_up)}** ({_pct(avg_uh, avg_up)} vs VLEN) "
      f"| **{_ms(avg_uc)}** ({_pct(avg_uh, avg_uc)} vs VLEN) |")
    w("")

    # ------------------------------------------------------------------
    # Section 2: synthetic throughput / size
    # ------------------------------------------------------------------
    if synth:
        w("## Synthetic session benchmark (`benchmark.py`)")
        w("")
        w("Workload: `add_turn` + `add_tool_call` per turn, `flush_every=1`.")
        w("")

        backends = [b for b in synth if not b.endswith("_lazy")]
        sizes = sorted(int(s) for s in next(iter(synth.values()))["text"].keys())
        scenarios = [("text", "text-only"), ("data", "array-heavy")]

        # Write throughput
        for scenario_key, scenario_label in scenarios:
            w(f"### Write throughput — {scenario_label} (turns/sec)")
            w("")
            w("| N | " + " | ".join(b for b in backends) + " |")
            w("|---|" + "|".join("---:" for _ in backends) + "|")
            for n in sizes:
                row = f"| {n:,} |"
                for b in backends:
                    tps = synth[b][scenario_key][str(n)]["throughput_tps"]
                    row += f" {tps:,.0f} |"
                w(row)
            w("")

        # File size at largest N
        max_n = str(max(sizes))
        w(f"### File size at N={max(sizes):,} (kB)")
        w("")
        w("| Scenario | " + " | ".join(backends) + " |")
        w("|----------|" + "|".join("---:" for _ in backends) + "|")
        for scenario_key, scenario_label in scenarios:
            row = f"| {scenario_label} |"
            for b in backends:
                kb = synth[b][scenario_key][max_n]["size_bytes"] / 1e3
                row += f" {kb:.0f} |"
            w(row)
        w("")

        # Context latency at largest N
        w(f"### get_recent_context(20) latency at N={max(sizes):,} (ms)")
        w("")
        w("| Scenario | " + " | ".join(backends) + " |")
        w("|----------|" + "|".join("---:" for _ in backends) + "|")
        for scenario_key, scenario_label in scenarios:
            row = f"| {scenario_label} |"
            for b in backends:
                ms = synth[b][scenario_key][max_n]["context_ms"]
                row += f" {ms:.2f} |"
            w(row)
        w("")

    # ------------------------------------------------------------------
    # Key findings summary
    # ------------------------------------------------------------------
    w("## Key findings")
    w("")
    w("| Metric | Winner | Notes |")
    w("|--------|--------|-------|")
    w(f"| File size | **packed** | ~{abs((avg_pk/avg_h5-1)*100):.0f}% smaller than VLEN; "
      f"compound ≈ VLEN (content still VLEN in both) |")
    w(f"| Write throughput | **compound** | "
      f"Compound: one resize+write vs. 9 separate in parallel layouts |")
    w(f"| get_recent_context latency | **compound** | "
      f"One compound slice vs. 7 parallel dataset reads |")
    w(f"| total_usage() latency | **VLEN / packed** | "
      f"Compound reads full chunks to reach 4 int fields; "
      f"parallel layout has a standalone numeric-only usage dataset |")
    w(f"| Schema complexity | **VLEN** | Simplest; compound adds nested dtype; "
      f"packed adds offset-index datasets |")
    w("")

    OUT.write_text("\n".join(lines) + "\n")
    print(f"wrote {OUT}")


if __name__ == "__main__":
    main()
